package test.test;

public class Calculator {
	
	public float sum(float x, float y) {
		return x + y;
	}
	
	public float subtraction(float x, float y) {
		return x - y;
	}
	
	public float product(float x, float y) {
		return x * y;
	}
	
	public float division(float x, float y) {
		return x / y;
	}

}
